    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String validacion;
        validacion = gdb.validar(id.getText(), pass1, pass2);
        RegistrarBedel esta=this;
        switch (validacion){
            case "confirmacion":
                esta.setAlwaysOnTop(true);
                ErrorCoincidenciaContrase単as ecc = new ErrorCoincidenciaContrase単as();
                esta.setEnabled(false);
                ecc.addWindowListener(new WindowAdapter(){
                    public void windowClosed(WindowEvent e){
                        esta.setEnabled(true);
                        esta.username.requestFocus();
                    }
                });
            break;
            case "coincidencia":
                esta.setAlwaysOnTop(true);
                ErrorCondicionesDeContrase単as eccoi = new ErrorCondicionesDeContrase単as();
                esta.setEnabled(false);
                eccoi.addWindowListener(new WindowAdapter(){
                    public void windowClosed(WindowEvent e){
                        esta.setEnabled(true);
                        esta.username.requestFocus();
                    }
                });
            break;
            case "politicas":
                esta.setAlwaysOnTop(true);
                ErrorRegistrarBedel erb = new ErrorRegistrarBedel();
                esta.setEnabled(false);
                erb.addWindowListener(new WindowAdapter(){
                    public void windowClosed(WindowEvent e){
                        esta.setEnabled(true);
                        esta.username.requestFocus();
                    }
                });
            break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!(gdb.camposVacios(username.getText(), name.getText(), last.getText(), id.getText(), turno.getSelectedItem().toString(), pass1, pass2))){
            RegistrarBedel esta=this;
            esta.setAlwaysOnTop(true);
            ConfirmacionCancelarModificar ccm = new ConfirmacionCancelarModificar();
            esta.setEnabled(false);
            ccm.getCancelar().addActionListener(new ActionListener(){
            public void actionPerformed(ActionEvent e){
                esta.setEnabled(true);
                esta.username.requestFocus();
                esta.username.grabFocus();
            }
            });
            ccm.getConfirmar().addActionListener(new ActionListener(){
            public void actionPerformed(ActionEvent e){
                esta.dispose();
            }
            });
        }else{
            this.dispose();
            OpcionesDelBedel opc = new OpcionesDelBedel();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

